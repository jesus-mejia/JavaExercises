// Characters cab be handled like integers

class CharAritDemo{
	public static void main(String args[]){
		char ch;
		
		ch = 'X';
		System.out.println("Ch contains: " + ch);
		
		ch++; // increment ch
		System.out.println("ch is now: " + ch);
		
		ch = 90; // give ch the value Z
		System.out.println("ch is now: " + ch);
	}
}